function applyTranslations(lang) {
  document.querySelectorAll("[data-key]").forEach((el) => {
    const key = el.getAttribute("data-key");
    if (translations[lang][key] !== undefined) {
      el.innerHTML = translations[lang][key];
    }
  });
  document.documentElement.lang = lang;
}

function setLanguage(lang) {
  applyTranslations(lang);
  localStorage.setItem("portfolioLang", lang);
  document.getElementById("langFr").classList.toggle("active", lang === "fr");
  document.getElementById("langEn").classList.toggle("active", lang === "en");
  const frame = document.getElementById("cvFrame");
  const link = document.getElementById("cvOpenNewTab");
  if (frame && link) {
    frame.dataset.src = translations[lang].cvFile;
    link.href = translations[lang].cvFile;
    if (document.getElementById("cvModal").classList.contains("open")) {
      frame.src = translations[lang].cvFile;
    }
  }
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("portfolioLang") || "fr";
  setLanguage(saved);
});